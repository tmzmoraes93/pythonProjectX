primeiraNota = input("Digite a primeira nota: ")
segundaNota = input("Digite a segunda nota: ")
terceiraNota = input("Digite a terceira nota: ")
quartaNota = input("Digite a quarta nota: ")

nota1 = float(primeiraNota)
